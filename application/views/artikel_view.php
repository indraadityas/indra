      <h1>Laravel</h1>
      <p class="w3-large">Laravel adalah sebuah framework PHP yang dirilis dibawah lisensi MIT, dibangun dengan konsep MVC (model view controller). Laravel adalah pengembangan website berbasis
        MVP yang ditulis dalam PHP yang dirancang untuk meningkatkan kualitas perangkat lunak dengan mengurangi biaya pengembangan awal dan biaya pemeliharaan, dan untuk meningkatkan
        pengalaman bekerja dengan aplikasi dengan menyediakan sintaks yang ekspresif, jelas dan menghemat waktu.</p>
      <p class="w3-large">MVC adalah sebuah pendekatan perangkat lunak yang memisahkan aplikasi logika dari presentasi. MVC memisahkan aplikasi berdasarkan komponen- komponen aplikasi, seperti :
        manipulasi data, controller, dan user interface.</p>
      <p class="w3-large"><strong>Beberapa fitur yang terdapat di Laravel :</strong></p>
      <p class="w3-large">
        <li>Reverse Routing, mendefinisikan relasi atau hubungan antara Link dan Route.</li>
        <li>Restful controllers, memisahkan logika dalam melayani HTTP GET and POST.</li>
        <li>Class Auto Loading, menyediakan loading otomatis untuk class PHP.</li>
        <li>Migration, menyediakan sistem kontrol untuk skema database.</li>
        <li>Unit Testing, banyak tes untuk mendeteksi dan mencegah regresi.</li>
        <li>Automatic Pagination, menyederhanakan tugas dari penerapan halaman.</li>
      </p>
